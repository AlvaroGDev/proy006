package es.cic._5.proy006;

public class Tarea {

    private long id;
    private String nombreTarea;
    private String descTaera;
    private String personaTarea;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getNombreTarea() {
        return nombreTarea;
    }

    public void setNombreTarea(String nombreTarea) {
        this.nombreTarea = nombreTarea;
    }

    public String getDescTaera() {
        return descTaera;
    }

    public void setDescTaera(String descTaera) {
        this.descTaera = descTaera;
    }

    public String getPersonaTarea() {
        return personaTarea;
    }

    public void setPersonaTarea(String personaTarea) {
        this.personaTarea = personaTarea;
    }

}
